# List demo
month_names = ["jan", "feb", "mar", 2021,5]
# finding the type of variable
print(type(month_names))
# finding the length of list
print(len(month_names))
# printing the last value in list
print(month_names[len(month_names)-1])
print(month_names[-1])
print(month_names[4])
# printing center position value from the list
print(month_names[len(month_names)//2])

